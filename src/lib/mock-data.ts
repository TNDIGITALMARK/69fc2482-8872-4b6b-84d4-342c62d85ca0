// Function to get predictions with filtering
export function getMockPredictions(options?: {
  status?: 'active' | 'pending' | 'resolved' | 'cancelled';
  category?: string;
  limit?: number;
}): Prediction[] {
  let results = [...mockPredictions];

  if (options?.status) {
    results = results.filter(p => p.status === options.status);
  }

  if (options?.category) {
    results = results.filter(p => p.category === options.category);
  }

  if (options?.limit) {
    results = results.slice(0, options.limit);
  }

  return results;
}

// Function to get trending predictions (sorted by participant count)
export function getMockTrendingPredictions(limit: number = 10): Prediction[] {
  return [...mockPredictions]
    .sort((a, b) => b.participant_count - a.participant_count)
    .slice(0, limit);
}

// Function to get a single prediction by ID
export function getMockPrediction(id: string): Prediction | undefined {
  return mockPredictions.find(p => p.id === id);
}
